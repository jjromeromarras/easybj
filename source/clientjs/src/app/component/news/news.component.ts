import { Component } from '@angular/core';
import { BroadcasterService } from 'ng-broadcaster';
import { eWorkAreaType } from '../../model/interfaz/enums/eworkareatype';

@Component(
    {
        selector: 'app-news',
        templateUrl: './news.component.html',
    }
)

export class NewsComponent {

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PRIVATE FIELDS
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private subscriber: any;

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    constructor(private messageeventservice: BroadcasterService) {
        this.registerTypeBroadcast();
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PRIVATE METHODS
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private registerTypeBroadcast() {
        this.subscriber = this.messageeventservice.on<any>('addMenuComponent')
            .subscribe(message => {
                if (message === eWorkAreaType.News) {
                    this.messageeventservice.broadcast('addMenuGroup', undefined);
                }
            });


    }
}